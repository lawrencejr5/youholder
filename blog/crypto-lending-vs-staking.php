                        <h2><strong>Staking vs. lending: understanding the risks</strong></h2>
                        <p>Just like with any other trading strategy, both staking and lending crypto comes with inherent risks. 
                        </p>
                        <h3><strong>Crypto staking risks</strong></h3>
                        <ul role="list">
                            <li><strong>Volatility: </strong>If you stake crypto and its prices drop significantly, the value you lose
                                can outweigh whatever interest you earned on it. <br /><br /></li>
                            <li><strong>Minimum staking requirements:</strong> Staking requires you to deposit your crypto for a
                                minimum period. If you suddenly need to use your crypto during that time, you won’t have access to it.
                                <br /><br />
                            </li>
                            <li><strong>Cannot withdraw immediately: </strong>Even after requesting to un-stake your crypto, you’ll
                                have to wait for the un-staking period to pass. This is usually around seven days. <br /><br /></li>
                            <li><strong>Technical risks: </strong>The staking pool you’re in may have its servers go offline, during
                                which you can’t earn rewards. If your staking pool is too small, it might fail. If it grows too large,
                                the blockchain may limit rewards.</li>
                        </ul>
                        <h3><strong>Risks of lending</strong></h3>
                        <ul role="list">
                            <li><strong>Margin calls: </strong>if the cryptocurrency you’ve put up as collateral drops in value, it
                                may trigger a margin call. This could lead to you needing to add collateral to keep the loan.</li>
                        </ul>
