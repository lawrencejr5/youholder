      <h1 class="h1 hero blog">USDC vs. USDT: Which Stablecoin to Use</h1>
      <div class="post-content-data w-clearfix">
        <div class="post-content-data-author">Anthony Cerullo</div>
        <div class="post-content-data-published">August 7, 2023</div>
      </div>
