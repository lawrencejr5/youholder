        <div class="column-compensation post-content-left w-col w-col-8"><img alt="A photo of the two logos (USDC and USDT)" src="https://assets-global.website-files.com/5cc1a690df4e901766e92dcd/64d087946577cae16153b659_USDC_USDT.jpeg" sizes="(max-width: 479px) 92vw, (max-width: 767px) 95vw, (max-width: 991px) 61vw, (max-width: 1439px) 59vw, 826.6640625px" srcset="https://assets-global.website-files.com/5cc1a690df4e901766e92dcd/64d087946577cae16153b659_USDC_USDT-p-500.jpeg 500w, https://assets-global.website-files.com/5cc1a690df4e901766e92dcd/64d087946577cae16153b659_USDC_USDT-p-800.jpeg 800w, https://assets-global.website-files.com/5cc1a690df4e901766e92dcd/64d087946577cae16153b659_USDC_USDT-p-1080.jpeg 1080w, https://assets-global.website-files.com/5cc1a690df4e901766e92dcd/64d087946577cae16153b659_USDC_USDT.jpeg 1600w" class="post-pic" />
