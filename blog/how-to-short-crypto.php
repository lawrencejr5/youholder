                        <p>These price crashes cost many crypto investors to lose money. Opening a <a href="https://www.youhodler.com/blog/short-position-vs-long-position">short position</a> allows
                            investors to gain from a price crash, instead of having to wait for the next peak. It’s also a great way
                            to add some balance to your portfolio, enabling you to diversify your tactics during a bear market.</p>
                        <h2><strong>Short crypto example</strong></h2>
                        <p>Let’s walk through an example of how you can short Bitcoin today. </p>
                        <ul role="list">
                            <li>Say you have 5 Bitcoins when the price is $40,000</li>
                            <li>You want to short-sell them. </li>
                            <li>This means you borrow 5 Bitcoins and sell them for a total of $200,000.</li>
                            <li>The price of Bitcoin drops to $35,000</li>
                            <li>You buy back the 5 Bitcoins to repay your loan at 5 x $35,000 = $175,000</li>
                            <li>Your total profit is $200,000 - $175,000 = $25,000</li>
                        </ul>
