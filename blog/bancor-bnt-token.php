                        <p>Over the past month, we saw BNT transition into a bullish trajectory after several months of quiet
                            consolidation. Currently, the token is trading at $.43, up 13.8% on the month but down 21.60% on the week.
                            <br /><br />After spiking up considerably at the beginning of August, BNT went through several days of
                            volatile spikes before settling on a steady decline starting on August 11. 
                        </p>
                        <p>The main reason for this is the release of Bancor’s new trading protocol, <strong>Carbon</strong>. On
                            August 6th, Bancor <a href="https://blog.bancor.network/a-primer-on-bancors-new-trading-protocol-carbon-5bc7d6a1a65f">released
                                a primer</a> on this new trading protocol, which they say is<br /><em>‍</em></p>
                        <blockquote><em>“a new groundbreaking approach to on-chain trading and liquidity.”</em></blockquote>
                        <p>With many trading experts praising these advancements, one can expect the BNT price to be active in the
                            coming weeks. </p>
                        <h2><strong>Institutional investors bullish on Ethereum (for now)</strong></h2>
                        <p>Similar Ethereum-based DeFi tokens are having success like BNT. One possible explanation is the <a href="https://www.investopedia.com/asset-managers-file-future-etfs-7568646">multiple asset management
                                firms</a> that have applied to launch Ether-based exchange-traded funds (ETFs).</p>
                        <p>Furthermore, <strong>PayPal,</strong> one of the world’s largest global payment service providers
                            launched a new dollar-based stablecoin <a href="https://www.paypal.com/us/digital-wallet/manage-money/crypto/pyusd">(PYUSD</a>) on the Ethereum
                            mainnet. With a user base of 400 million users, this means more eyes on Ethereum and more active users,
                            which bodes well for Ethereum-based projects like Bancor. </p>
