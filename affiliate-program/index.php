      const params = getUrlParams();
      const paramsKeys = Object.keys(params).filter(Boolean);
      const paramsKeysMap = {
        // [eventKey from landing page without register match] : [eventKey to marketing]
        utm_source: 'af_channel',
        cid: 'cid',
      };

      classNames.forEach(className => {
        const links = document.getElementsByClassName(className);

        for (let link of links) {
          const url = new URL(link);

          paramsKeys.forEach(key => {
            url.searchParams.set(
              // Parameters that are in paramsKeysMap are case-insensitive and are transformed into the    required key for marketing
              paramsKeysMap[key.toLowerCase()] ?? key,
              params[key]
            );
          });

          link.href = url.href;
        }
      });
