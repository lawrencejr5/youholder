      if (ukPopup) {
        ukPopup.style.display = 'block';

        var button = document.getElementById('UK_POPUP_BUTTON');

        if (button) {
          function handler() {
            setToStorage("KNOW_IS_FROM_UK_AGREED");
            button.removeEventListener('click', handler);
            ukPopup.style.display = 'none';
          };
          button.addEventListener('click', handler);
        }
      }
    }

    var UK_POPUP_STATUS = getFromStorage();

    if (UK_POPUP_STATUS === "UNKNOWN") {
      fetch("https://app.youhodler.com/api/v1/auth/country")
        .then((response) => response.json())
        .then((data) => {
          var countryCode = data.iso3CountryCode || "UNKNOWN";

          if (countryCode === "GBR") {
            makePopupVisible();
            setToStorage("KNOW_IS_FROM_UK_NOT_YET_AGREED");
          } else {
            setToStorage("KNOW_IS_NOT_FROM_UK");
          }
        })
        .catch(() => {});
    }

    if (UK_POPUP_STATUS === "KNOW_IS_FROM_UK_NOT_YET_AGREED") {
      makePopupVisible();
    }
  </script>
